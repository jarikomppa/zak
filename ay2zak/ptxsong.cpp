void PtXSong::load_memory()
{
   const unsigned pl_size = sizeof(pt3_player);
   make_player(0x8000, 0x8005);

   // concatenate pt2/3 player and song data in memory
   unsigned dst = 0x8000;
   for (unsigned i = 0; i < pl_size; i++)
      sys_wm(dst++, pt3_player[i]);
   for (unsigned j = 0; j < filesize; j++)
      sys_wm(dst++, filedata[j]);
}
